public class Item {

    private String name;

    public Item(String name) {
        this.name = name;
    }

    /**
     * @return the name
     */
    public String getname() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setname(String name) {
        this.name = name;
    }
}